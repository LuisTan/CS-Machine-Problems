package GAME;
/**
* This enum Suit restricts the value of the Card objects' classification of suit into four - Diamonds, Hearts, Spades and Clubs. Public access.
*/
public enum Suit{
	/**
	* Sets the suit value of a Card object to "DIAMONDS".
	*/
	DIAMONDS,

	/**
	* Sets the suit value of a Card object to "HEARTS".
	*/

	HEARTS,
	/**
	* Sets the suit value of a Card object to "SPADES".
	*/
	SPADES,
	/**
	* Sets the suit value of a Card object to "CLUBS".
	*/
	CLUBS,
}